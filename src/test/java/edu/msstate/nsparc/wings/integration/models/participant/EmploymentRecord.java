package edu.msstate.nsparc.wings.integration.models.participant;

public class EmploymentRecord {
    private String employerName;
    private String jobTitle;
    private String dateStartWorking;
    private String hoursWeek;
    private String dateEndWorking;
    private String moneyEarn;
    private boolean specialLicense;
    private String reasonForLeaving;
    private String lineOne;
    private String lineTwo;
    private String employerCity;
    private String employerState;
    private String employerZipCode;
    private String jobDescription;
    private String osocCode;
    private String period;

    public String getPeriod() {
        return period;
    }

    public void setPeriod(String period) {
        this.period = period;
    }

    public String getEmployerName() {
        return employerName;
    }

    public void setEmployerName(String employerName) {
        this.employerName = employerName;
    }

    public String getJobTitle() {
        return jobTitle;
    }

    public void setJobTitle(String jobTitle) {
        this.jobTitle = jobTitle;
    }

    public String getDateStartWorking() {
        return dateStartWorking;
    }

    public void setDateStartWorking(String dateStartWorking) {
        this.dateStartWorking = dateStartWorking;
    }

    public String getHoursWeek() {
        return hoursWeek;
    }

    public void setHoursWeek(String hoursWeek) {
        this.hoursWeek = hoursWeek;
    }

    public String getDateEndWorking() {
        return dateEndWorking;
    }

    public void setDateEndWorking(String dateEndWorking) {
        this.dateEndWorking = dateEndWorking;
    }

    public String getMoneyEarn() {
        return moneyEarn;
    }

    public void setMoneyEarn(String moneyEarn) {
        this.moneyEarn = moneyEarn;
    }

    public void setSpecialLicense(boolean specialLicense) {
        this.specialLicense = specialLicense;
    }

    public String getReasonForLeaving() {
        return reasonForLeaving;
    }

    public void setReasonForLeaving(String reasonForLeaving) {
        this.reasonForLeaving = reasonForLeaving;
    }

    public String getLineOne() {
        return lineOne;
    }

    public void setLineOne(String lineOne) {
        this.lineOne = lineOne;
    }

    public String getLineTwo() {
        return lineTwo;
    }

    public void setLineTwo(String lineTwo) {
        this.lineTwo = lineTwo;
    }

    public String getEmployerCity() {
        return employerCity;
    }

    public void setEmployerCity(String employerCity) {
        this.employerCity = employerCity;
    }

    public void setEmployerState(String employerState) {
        this.employerState = employerState;
    }

    public String getEmployerZipCode() {
        return employerZipCode;
    }

    public void setEmployerZipCode(String employerZipCode) {
        this.employerZipCode = employerZipCode;
    }

    public String getJobDescription() {
        return jobDescription;
    }

    public void setJobDescription(String jobDescription) {
        this.jobDescription = jobDescription;
    }

    public String getOsocCode() {
        return osocCode;
    }

    public void setOsocCode(String osocCode) {
        this.osocCode = osocCode;
    }
}

